public class FancyTable
{
	public static void main(String[] args)
	{
		final String BORDER = "+---------------+---------------+---------------+"; // create border around table
		System.out.println(BORDER);
		System.out.println("| First Name \t| Last Name \t| Initials \t|"); // table header
		System.out.println(BORDER);
		System.out.println("| Drew\t\t| Brown\t\t| dB\t\t|"); // table row
		System.out.println(BORDER);	
	}
}